package lab01_duixiang;

/**
 * 类的定义
 */
public class Part1_define {
    //成员变量
    private Long id;
    private String name;
    private String addr;
    //成员方法
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }
}
