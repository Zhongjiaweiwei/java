package lab01_foundation;

/**
 * 数据类型
 */
public class Part4_ShuJuLeiXing {
    public static void main(String[] args) {
        //整数类型
        byte b='a';
        System.out.println(b);//97
        short s=2;
        int i=3;
        long l=4;
        //浮点类型
        float f=3.14f;//float数值后要加f
        double d=2.23;
        //字符类型
        char c='a';//a
        System.out.println(c);
        //布尔类型
        boolean a=true;
    }

}
